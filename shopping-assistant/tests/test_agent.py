class TestSingleUseEnforcement:
    def test_code_cannot_be_redeemed_twice_by_same_user(self):
        """A code that has already been redeemed is rejected on second attempt."""
        redeem = tool()
        first = redeem("user_001", "WELCOME50")
        second = redeem("user_001", "WELCOME50")
        assert "Success" in first
        assert "already been redeemed" in second

    def test_code_cannot_be_redeemed_by_different_user_after_first_use(self):
        """Once a code is redeemed by user_001, user_002 cannot use the same code."""
        redeem = tool()
        redeem("user_001", "WELCOME50")
        result = redeem("user_002", "WELCOME50")
        assert "already been redeemed" in result

    def test_redeemed_flag_is_persisted_in_store(self):
        """After redemption, the in-memory store reflects redeemed=True."""
        from app import agent as agent_module

        tool()("user_001", "WELCOME50")
        assert agent_module._DISCOUNT_CODES["WELCOME50"]["redeemed"] is True

    def test_second_code_is_independent_of_first(self):
        """Redeeming WELCOME50 does not affect SUMMER20's redeemed state."""
        from app import agent as agent_module

        tool()("user_001", "WELCOME50")
        assert agent_module._DISCOUNT_CODES["SUMMER20"]["redeemed"] is False


