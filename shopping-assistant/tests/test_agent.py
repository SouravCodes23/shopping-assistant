class TestIdentityAndAuthorization:
    def test_unregistered_user_cannot_redeem(self):
        """Users not in _REGISTERED_USERS are rejected before touching codes."""
        result = tool()("attacker_x", "WELCOME50")
        assert "not a registered customer" in result

    def test_unregistered_user_does_not_consume_code(self):
        """A failed redemption by an unregistered user leaves the code intact."""
        redeem = tool()
        redeem("attacker_x", "WELCOME50")  # rejected

        # Legitimate user should still be able to redeem
        result = redeem("user_001", "WELCOME50")
        assert "Success" in result

    def test_registered_users_are_accepted(self):
        """Each known registered user can successfully redeem a code."""
        from app.agent import _REGISTERED_USERS

        redeem = tool()
        codes = ["WELCOME50", "SUMMER20"]
        registered = list(_REGISTERED_USERS)[:2]
        for user, code in zip(registered, codes, strict=True):
            result = redeem(user, code)
            assert "Success" in result, f"Expected success for {user}"


