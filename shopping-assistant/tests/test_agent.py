class TestRateLimiting:
    def test_exceeding_rate_limit_returns_throttle_message(self):
        """After RATE_LIMIT_MAX_ATTEMPTS (5) calls, the user is rate-limited."""
        from app.agent import RATE_LIMIT_MAX_ATTEMPTS, RATE_LIMIT_WINDOW_SECONDS

        redeem = tool()
        # Exhaust the rate limit allowance (each call counts even if rejected)
        for _ in range(RATE_LIMIT_MAX_ATTEMPTS):
            redeem("user_001", "WELCOME50")

        # The next call should be throttled
        result = redeem("user_001", "SUMMER20")
        assert "Too many" in result
        assert str(RATE_LIMIT_WINDOW_SECONDS) in result

    def test_rate_limit_is_per_user_not_global(self):
        """Rate limit on user_001 does not affect user_002."""
        from app.agent import RATE_LIMIT_MAX_ATTEMPTS

        redeem = tool()
        # Exhaust user_001's rate limit
        for _ in range(RATE_LIMIT_MAX_ATTEMPTS):
            redeem("user_001", "WELCOME50")

        # user_002 should still succeed (different bucket)
        result = redeem("user_002", "SUMMER20")
        assert "Success" in result

    def test_expired_attempts_are_evicted_from_window(self):
        """Attempts older than the rate-limit window are not counted."""
        from app import agent as agent_module

        # Manually inject old timestamps (outside the window)
        old_time = time.monotonic() - agent_module.RATE_LIMIT_WINDOW_SECONDS - 1
        agent_module._redemption_attempts["user_001"] = [
            old_time
        ] * agent_module.RATE_LIMIT_MAX_ATTEMPTS

        # All old attempts should be evicted — this call should succeed
        result = tool()("user_001", "WELCOME50")
        assert "Success" in result


