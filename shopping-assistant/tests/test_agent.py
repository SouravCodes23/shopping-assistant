# ===========================================================================


class TestConcurrency:
    def test_concurrent_redemptions_only_one_succeeds(self):
        """Under concurrent load, WELCOME50 can only be redeemed once."""
        from app import agent as agent_module

        # Register a unique user ID for each thread to avoid per-user rate limiting (max 5)
        registered_users = [f"user_concurrent_{i:03d}" for i in range(10)]
        for user_id in registered_users:
            agent_module._REGISTERED_USERS.add(user_id)

        redeem = tool()
        results = []
        lock = threading.Lock()

        def attempt(user_id):
            result = redeem(user_id, "WELCOME50")
            with lock:
                results.append(result)

        threads = [
            threading.Thread(target=attempt, args=(user_id,))
            for user_id in registered_users
        ]
        for t in threads:
            t.start()
        for t in threads:
            t.join()

        success_count = sum(1 for r in results if "Success" in r)
        already_redeemed_count = sum(1 for r in results if "already been redeemed" in r)

        assert success_count == 1, f"Expected exactly 1 success, got {success_count}"
        assert already_redeemed_count == 9, (
            f"Expected 9 rejections, got {already_redeemed_count}"
        )
